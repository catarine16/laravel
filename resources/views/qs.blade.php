<h1 style="text-align: center; margin-top: 95px;"> QUEM SOMOS? </h1>
<p style="font-size:15pt"> A ETEC Zona Leste, fundada em 2008 na Avenida Águia de Haia em São Paulo, é hoje um marco educacional e de infraestrutura na região. Inicialmente, o local seria destinado à construção de um presídio, mas a comunidade local, reconhecendo a necessidade de um centro educacional, mobilizou-se contra essa proposta. Graças ao apoio da comunidade e à coleta de assinaturas em abaixo-assinados, o terreno foi destinado à construção das instituições de ensino ETEC e FATEC Zona Leste, pertencentes ao Centro Paula Souza.</p>
<p style="font-size:15pt">   Ao longo dos anos, a escola tem se adaptado às demandas da sociedade e do mercado, expandindo sua oferta de cursos e modernizando suas instalações. Contando com uma equipe dedicada de professores e funcionários, a ETEC Zona Leste oferece uma variedade de cursos técnicos, proporcionando aos alunos uma formação sólida e atualizada.</p>
